  def get_config(key) do
    raw_config = Keyword.get(@configurations, key, [])
    x_size = length(Enum.at(raw_config, 0))

    raw_config
    |> List.flatten()
    |> Enum.with_index()
    |> Enum.map(fn {x, i} -> {{rem(i, x_size), div(i, x_size)}, x} end)
    |> Enum.into(%{})
  end
